# \n : 문장 내 줄바꿈
print("백문이 불여일견\n백견이 불여일타")

# \" \" : 문장 내에서 ""를 출력해주는 역할
# \' \' : 문장 내에서 ''를 출력해주는 역할
# 저는 "나도코딩"입니다.
print('저는 "나도코딩"입니다.')
print("저는 \"나도코딩\"입니다.")
print("저는 \'나도코딩\'입니다.")

# \\ : 문장 내에서 \ 출력
print(" D:\\Documents\\GitHub\\start")

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine") # Red 대신 Pine 출력

# \b : 백스페이스 (한글자 삭제)
print("Redd\bApple") # \b 바로 앞글자 삭제 (d 삭제됨)

# \t : 탭 (공백 출력)
print("Red\tApple")
